import java.util.*;
import java.util.ArrayList; // import the ArrayList class
class Account {

    private final String name;
    private final int pin;

    public Account(String name, int pin) {
        this.name = name;
        this.pin = pin;
    }

    public String getName() {
        return name;
    }

    public int getPin() {
        return pin;
    }




}